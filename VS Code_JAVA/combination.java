public class combination {

    static void main(String[] args) {
        int a = 1;
        int b = 1;
        int c = 1;
        for (a = 1; a <= 3; a++) {
            for (b = 1; b <= 3; b++) {
                for (c = 1; c <= 3; c++) {
                    System.out.println(a + " " + b + " " + c);
                }
            }
        }
    }

}
